function Window.Show(windowName: string, pinned: boolean?, hideSideGuis: boolean?)
	Signals.Fire("DispatchAction", {
		type = "ShowWindow",
		value = windowName,
		pinned = pinned,
		hideSideGuis = hideSideGuis,
	})
end

function Window.Close(windowName: string)
	Signals.Fire("DispatchAction", {
		type = "CloseWindow",
		value = windowName,
	})
end

