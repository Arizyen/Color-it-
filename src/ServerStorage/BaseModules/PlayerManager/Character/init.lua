Signals.Connect("PlayerCharacterAdded", function(player, character)
	if not player or not character then
		return
	end

	local humanoid = character:WaitForChild("Humanoid", 60)
	if humanoid then
		MovementManager.Activate(player, character, humanoid)
	end
end)

